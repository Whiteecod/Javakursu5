package Gun13;

public class _02_WhileLoop {
    public static void main(String[] args) {
        // Ekrana 5 kez Merhaba Yazdırınız.

        int sayac=1;
        while(sayac<6) // iken yap : şartı yazıyoruz, dönme şartı
        {
            // her döndükçe yapılacaklar
            System.out.println(sayac+"Merhaba");

            sayac++;
        }



    }
}
